// Function to get products for subcategory with minimum count guarantee
export const getProductsForSubcategoryPage = (
  subcategoryKey: string,
  minCount: number = 6
): Product[] => {
  let subcategoryProducts = getProductsBySubcategory(subcategoryKey);

  // If we have enough products, return all of them
  if (subcategoryProducts.length >= minCount) {
    return subcategoryProducts;
  }

  // If we need more products, generate additional ones based on existing patterns
  const additionalProductsNeeded = minCount - subcategoryProducts.length;
  const baseProduct = subcategoryProducts[0];

  if (baseProduct) {
    // Generate additional products based on the existing product pattern
    for (let i = 0; i < additionalProductsNeeded; i++) {
      const variantNumber = subcategoryProducts.length + i + 1;
      const newProduct: Product = {
        id: `${baseProduct.id}-variant-${variantNumber}`,
        slug: `${baseProduct.slug}-variant-${variantNumber}`,
        categoryKey: baseProduct.categoryKey,
        subcategoryKey: baseProduct.subcategoryKey,
        image: baseProduct.image,
        datasheetUrl: baseProduct.datasheetUrl,
        specs: {
          ...baseProduct.specs,
          // Vary some specs to make products distinct
          model: `Model ${variantNumber}`,
          variant: `Variant ${variantNumber}`,
        },
        translationKeys: baseProduct.translationKeys ? {
          title: `${baseProduct.translationKeys.title}.variant${variantNumber}`,
          description: `${baseProduct.translationKeys.description}.variant${variantNumber}`,
          featuresKey: baseProduct.translationKeys.featuresKey
            ? `${baseProduct.translationKeys.featuresKey}.variant${variantNumber}`
            : undefined,
        } : undefined,
        title: baseProduct.title ? `${baseProduct.title} - Variant ${variantNumber}` : undefined,
        description: baseProduct.description,
        features: baseProduct.features,
      };
      subcategoryProducts.push(newProduct);
    }
  } else {
    // If no products exist, create default ones
    for (let i = 0; i < minCount; i++) {
      const productNumber = i + 1;
      const defaultProduct: Product = {
        id: `${subcategoryKey}-product-${productNumber}`,
        slug: `${subcategoryKey}-product-${productNumber}`,
        categoryKey: "default",
        subcategoryKey: subcategoryKey,
        image: "default-product",
        specs: {
          model: `Model ${productNumber}`,
          type: "Standard",
          application: "Industrial",
        },
        translationKeys: {
          title: `products:${subcategoryKey}.product${productNumber}.title`,
          description: `products:${subcategoryKey}.product${productNumber}.description`,
          featuresKey: `products:${subcategoryKey}.product${productNumber}.features`,
        },
      };
      subcategoryProducts.push(defaultProduct);
    }
  }

  return subcategoryProducts.slice(0, minCount);
};

